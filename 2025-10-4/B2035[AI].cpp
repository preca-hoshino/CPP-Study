#include<bits/stdc++.h>
using namespace std;
int main()
{
    long long n = 0;
    cin >> n;
    if (n > 0)
    {
        cout << "positive";
    }
    else if (n == 0)
    {
        cout << "zero";
    }
    else
    {
        cout << "negative";
    }
    return 0;
}
